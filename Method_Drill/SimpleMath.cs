using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Method_Drill
{
    public class SimpleMath
    {
        public static int AddTwoNumbers(int userNumber)
        {
            return userNumber + 2;
        }

        public static int SubtractTwoNumbers(int userNumber)
        {
            return userNumber - 1;
        }

        public static int MultiplyTwoNumbers(int userNumber)
        {
            return userNumber * 2;
        }
    }
}
